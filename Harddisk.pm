sub new {
    my ($class, $devname)= @_;
    my $disk;
    open $disk, "<$devname" or croak "$devname: $!\n";
    binmode $disk;

    return bless $disk, $class;
}
sub readsector {
    my ($disk, $sectornr, $count)= @_;
    $count=1 unless defined $count;
    #printf("readsector(%d, %d) -> ofs %08lx\n", $sectornr, $count, 0x200*$sectornr);
    return "" if $count==0;
    sysseek($$disk, 0x200*$sectornr, SEEK_SET) or croak "diskseek to sector $sectornr\n";
    my $data;
    sysread($$disk,$data,0x200*$count) or croak "error reading $count sectors starting at $sectornr\n";
    return $data;
}

